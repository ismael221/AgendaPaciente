public class Consulta{
    
    private String paciente;
    private String cpf;
    private String telefone;
    private String data;
    private String jaEraPaciente;
    private String consultaRealizada;

    public String getPaciente() {
        return paciente;
    }

    public void setPaciente(String paciente) {
        this.paciente = paciente;
    }

    public String getCpf() {
        return cpf;
    }

    public void setCpf(String cpf) {
        this.cpf = cpf;
    }

    public String getTelefone() {
        return telefone;
    }

    public void setTelefone(String telefone) {
        this.telefone = telefone;
    }

    public String getData() {
        return data;
    }

    public void setData(String data) {
        this.data = data;
    }


    public String getJaEraPaciente() {
        return jaEraPaciente;
    }

    public void setJaEraPaciente(String jaEraPaciente) {
        this.jaEraPaciente = jaEraPaciente;
    }

    public String getConsultaRealizada() {
        return consultaRealizada;
    }

    public void setConsultaRealizada(String consultaRealizada) {
        this.consultaRealizada = consultaRealizada;
    }

 
    
    
}
